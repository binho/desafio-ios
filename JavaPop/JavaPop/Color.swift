import Foundation
import UIKit

enum Color {
    static var navigationBarBackgroundColor: UIColor { get { return UIColor.black } }
    static var navigationBarFontColor: UIColor { get { return UIColor.white } }
    
    static var customBlue: UIColor { get { return UIColor(hexString: "0b649f") } }
    static var customOrange: UIColor { get { return UIColor(hexString: "dd910c") } }
    static var customDarkGray: UIColor { get { return UIColor(hexString: "0a0a0a") } }
    static var customGray: UIColor { get { return UIColor(hexString: "a4a4a5") } }
}
